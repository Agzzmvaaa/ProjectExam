def add_formatted_paragraph(doc, text, style=None, bold=False, size=12):
    p = doc.add_paragraph(style=style)
    run = p.add_run(text)
    run.font.size = Pt(size)
    run.bold = bold
    return p


def main():
    doc = Document()

    sections = doc.sections
    for section in sections:
        section.top_margin = Cm(2)
        section.bottom_margin = Cm(2)
        section.left_margin = Cm(3)
        section.right_margin = Cm(1.5)

    title = doc.add_heading("Ответы на экзаменационные вопросы", level=0)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER

    subtitle = doc.add_paragraph()
    subtitle.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = subtitle.add_run("Дисциплина: «Теория радиотехнических сигналов»")
    run.font.size = Pt(12)
    run.italic = True

    doc.add_paragraph()

    for i, (question, answer) in enumerate(zip(QUESTIONS, ANSWERS), 1):
        q_heading = doc.add_heading(f"Вопрос {i}", level=1)
        q_heading.runs[0].font.size = Pt(14)

        q_para = doc.add_paragraph()
        q_run = q_para.add_run(question)
        q_run.bold = True
        q_run.font.size = Pt(12)

        a_para = doc.add_paragraph()
        a_run = a_para.add_run("Ответ:")
        a_run.bold = True
        a_run.font.size = Pt(12)

        for line in answer.strip().split("\n"):
            if line.strip():
                p = doc.add_paragraph(line.strip())
                p.paragraph_format.first_line_indent = Cm(1)
                for run in p.runs:
                    run.font.size = Pt(11)

        doc.add_paragraph()

    output_path = "/workspace/Ответы_на_экзаменационные_вопросы.docx"
    doc.save(output_path)
    print(f"Saved: {output_path}")
    print(f"Questions: {len(QUESTIONS)}, Answers: {len(ANSWERS)}")


if __name__ == "__main__":
    main()
